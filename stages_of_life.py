#conjunto de edades para crear if/elif/else.

age = 25

if age < 2:
	print("This person is a baby.")

elif age < 13:
	print("This person is a kid.")

elif age < 20:
	print("This person is a teenager.")

elif age < 65:
	print("This person is an adult.")

else:
	print("This person is an elder.")