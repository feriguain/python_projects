# if-elif-else block:

age = 12
print("Hi, I'm 12 years old. I would like to buy a ticket.\n")

if age < 4:
	print("If you are under 4 years, it will cost $0.\n")

elif age < 18:
	print("If you are between 4 years and 18 years, it will cost $25.\n")

else:
	print("If you're over 18, it will cost $45.\n")